import express from "express";
import { createServer as createViteServer } from "vite";
import path from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

async function startServer() {
  const app = express();
  const PORT = 3000;

  app.use(express.json());

  // API Routes
  app.get("/api/health", (req, res) => {
    res.json({ status: "ok", timestamp: new Date().toISOString() });
  });

  // Mock Telegram Stars Payment Endpoint
  app.post("/api/payments/create-invoice", (req, res) => {
    const { planId, userId } = req.body;
    // In a real app, you'd call Telegram Bot API createInvoice
    res.json({ 
      success: true, 
      invoiceUrl: "https://t.me/invoice/mock_stars_payment",
      message: "Payment initiated for plan: " + planId
    });
  });

  // Vite middleware for development
  if (process.env.NODE_ENV !== "production") {
    const vite = await createViteServer({
      server: { middlewareMode: true },
      appType: "spa",
    });
    app.use(vite.middlewares);
  } else {
    // Serve static files in production
    app.use(express.static(path.join(__dirname, "dist")));
    app.get("*", (req, res) => {
      res.sendFile(path.join(__dirname, "dist", "index.html"));
    });
  }

  app.listen(PORT, "0.0.0.0", () => {
    console.log(`AstraAI Server running on http://localhost:${PORT}`);
  });
}

startServer();
